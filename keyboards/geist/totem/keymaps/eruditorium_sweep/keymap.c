#ifdef TAPPING_TERM_PER_KEY
uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case GUI_A:
        case GUI_S:
        case ALT_S:
        case ALT_L:
            return TAPPING_TERM + 50;
        case CTL_D:
        case CTL_K:
            return TAPPING_TERM - 50;
        case SHT_F:
        case SHT_J:
        case SHT_Z:
            return TAPPING_TERM - 80;
        default:
            return TAPPING_TERM;
    }
}
#endif

